def show_keypoints(points_ori):
    points = points_ori.copy()
    img = np.zeros((1080,1920,3))
    points[::2] = points[::2]*1920
    points[1::2] = points[1::2]*1080
    points = points.astype(int)
    for i in range(18):
        img = cv2.circle(img, (int(points[2*i]), int(points[2*i+1])), 4, colors[i], thickness=-1)
    for i in range(len(limbSeq)):
        seq_i, seq_j = limbSeq[i][0]-1, limbSeq[i][1]-1
        point_i = np.array([points[2*seq_i], points[2*seq_i+1]])
        point_j = np.array([points[2*seq_j], points[2*seq_j+1]])
        if np.sum(point_i)>0 and np.sum(point_j)>0:
            img = cv2.line(img,(point_i[0],point_i[1]), (point_j[0], point_j[1]),colors[i])
    return img


class AverageMeter(object):
    """Computes and stores the average and current value"""

    def __init__(self):
        self.reset()

    def reset(self):
        self.cur_val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, cur_val):
        self.cur_val = cur_val
        self.sum += cur_val
        self.count += 1
        self.avg = self.sum / self.count

def compute_accuracy(labels, predect):
    num_right = 0
    for p, l in zip(predect, labels):
        if (l == 0 and p < 0.5) or (l == 1 and p > 0.5):
            num_right +=1
    return num_right/len(labels)

